export function getCatalogProviderId(providerId) {
  if (providerId === 'azure-openai') return 'openai';
  return providerId;
}

export function getProviderModelCatalog(providerId) {
  return LLM_PROVIDER_CATALOGS[getCatalogProviderId(providerId)] || null;
}

export function getProviderModelCatalogRows(providerId) {
  return getProviderModelCatalog(providerId)?.models || [];
}

export function getProviderModelCatalogEntry(providerId, modelId) {
  if (!modelId) return null;
  return getProviderModelCatalogRows(providerId).find((entry) => entry.id === modelId) || null;
}

export function providerHasModelCatalog(providerId) {
  return getProviderModelCatalogRows(providerId).length > 0;
}
